package Operators;

public class Arithmetic {
	
	public static void main(String[] args) {
		byte a = 20, b = 10;
		
		// addition 
		System.out.println("Value of a + b = "+ (a+b));
		
		// Multiplication
		System.out.println("Value of a * b = "+ (a*b));
		
		// Subtraction
		System.out.println("Value of a - b = "+ (a-b));
		
		//Division
		System.out.println("Value of a / b = "+ (a/b));
		
		// Module
		System.out.println("Value of a % b = "+ (a%b));
		
	}
	
	

}
