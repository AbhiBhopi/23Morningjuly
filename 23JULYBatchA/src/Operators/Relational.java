package Operators;

public class Relational {
	
	public static void main(String[] args) {
		
		int a = 40; 
		float b=70.89f;
		
		// value of a and b
		
		System.out.println("a value is "+ a + "B value is "+ b);
		
		// Greater than operator
		
		System.out.println(b>a);
		
		// Less than operator
		
		
		System.out.println(b<a);
		
		// == operator
		System.out.println(a==b);
		
		// != operator
		System.out.println(a!=b);
		
	}

}
